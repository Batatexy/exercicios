public class AparelhoTelefonico {
    public static void ligar() {
        System.out.println("Aparelho ligando...");
        System.out.println("Aparelho ligado. \n");
    }

    public static void desligar() {
        System.out.println("Aparelho desligando...");
        System.out.println("Aparelho desligado. \n");
    }

    public static void atender() {
        System.out.println("Atendendo ligação...");
        System.out.println("Ligação atendida.");
        System.out.println("Ligação encerrada. \n");
    }

    public static void iniciarCorreiVoz() {
        System.out.println("Iniciando correio de voz...");
        System.out.println("Correio de voz encerrado. \n");
    }
}
