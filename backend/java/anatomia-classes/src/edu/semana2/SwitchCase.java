package semana2;

public class SwitchCase {
    public static void main(String[] args) {
        int inteiro = 4;

        switch (inteiro) {
            case 1: {
                System.out.println(1);
                break;
            }
            case 2: {
                System.out.println(2);
                break;
            }
            case 3: {
                System.out.println(3);
                break;
            }
            case 4: {
                System.out.println(4);
                break;
            }
            case 5: {
                System.out.println(5);
                break;
            }
        }
    }
}